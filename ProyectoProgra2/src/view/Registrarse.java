    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        if(!"".equals(txtNombre.getText()) && !"".equals(txtPass.getText()) && !"".equals(txtUsuario.getText()) && !"".equals(txtPuesto.getText())){
            try {
                Usuario user=new Usuario();
                String pass= new String(txtPass.getPassword());
                user.setNombre(txtNombre.getText());
                user.setUsuario(txtUsuario.getText());
                user.setPass(pass);
                user.setPuesto(txtPuesto.getText());
                UsuarioBl.agregar(user);
                JOptionPane.showMessageDialog(rootPane, "Usuario registrado", "", JOptionPane.INFORMATION_MESSAGE);
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(rootPane, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(rootPane, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
            Loggin log =new Loggin();
            log.setVisible(true);
            dispose();
        }else{
            JOptionPane.showMessageDialog(rootPane, "Hay campos vacios", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

